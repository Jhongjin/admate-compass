-- AdMate Compass schema v1
-- Scope: schema-only preparation for Compass RAG/document indexes.
-- Gate 3 confirmed source document_chunks and ollama_document_chunks use vector(1024).
-- v1 explicitly excludes policy archive table candidates with different dimensions.
-- Do not run until Gate 4 schema-only execution is separately approved.

create schema if not exists compass;

create extension if not exists vector;
create extension if not exists "uuid-ossp";
create extension if not exists pgcrypto;

do $$
begin
  if not exists (
    select 1
    from pg_type t
    join pg_namespace n on n.oid = t.typnamespace
    where n.nspname = 'compass'
      and t.typname = 'vendor_enum'
  ) then
    create type compass.vendor_enum as enum ('META', 'GOOGLE', 'NAVER', 'KAKAO', 'X', 'TIKTOK');
  end if;
end $$;

create table if not exists compass.documents (
  id text primary key,
  title text not null,
  content text default '',
  type text not null check (type = any (array['file', 'url', 'pdf', 'docx', 'txt'])),
  document_url text,
  created_at timestamp with time zone default now(),
  updated_at timestamp with time zone default now(),
  chunk_count integer default 0,
  status text default 'pending' check (status = any (array['pending', 'processing', 'completed', 'indexed', 'failed'])),
  url text,
  size bigint default 0,
  file_size bigint default 0,
  file_type text default '',
  source_vendor text default 'META',
  split_status jsonb,
  main_document_id text references compass.documents(id),
  original_file_name text,
  sanitized_file_name text,
  metadata jsonb default '{}'
);

create table if not exists compass.document_metadata (
  id text primary key references compass.documents(id),
  title text not null,
  type text not null check (type = any (array['pdf', 'docx', 'txt', 'url'])),
  size bigint not null,
  uploaded_at timestamp with time zone not null,
  processed_at timestamp with time zone,
  status text not null default 'pending' check (status = any (array['pending', 'processing', 'completed', 'failed'])),
  chunk_count integer default 0,
  embedding_count integer default 0,
  metadata jsonb default '{}',
  created_at timestamp with time zone default now(),
  updated_at timestamp with time zone default now(),
  original_file_name text
);

create table if not exists compass.document_chunks (
  id text primary key,
  document_id text not null,
  chunk_id integer not null,
  content text not null,
  -- Gate 3 confirmed source dimension: vector(1024).
  embedding vector(1024),
  created_at timestamp with time zone default now(),
  metadata jsonb,
  parent_chunk_id text,
  hierarchy_level text check (
    hierarchy_level = any (array['document', 'section', 'paragraph', 'sentence'])
    or hierarchy_level is null
  )
);

create table if not exists compass.ollama_document_chunks (
