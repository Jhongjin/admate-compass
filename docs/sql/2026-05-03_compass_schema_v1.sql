-- Create vector indexes after data load if source table is large.
-- Creating them before data import is valid but can slow large imports.
create index if not exists idx_compass_document_chunks_embedding
on compass.document_chunks
using ivfflat (embedding vector_cosine_ops)
with (lists = 100);

create index if not exists idx_compass_ollama_chunks_embedding
on compass.ollama_document_chunks
using ivfflat (embedding vector_cosine_ops)
with (lists = 100);

create or replace function compass.search_ollama_documents(
  query_embedding vector(1024),
  match_threshold float default 0.001,
  match_count int default 5
)
returns table (
  chunk_id text,
  content text,
  metadata jsonb,
  similarity float,
  embedding vector(1024)
)
language plpgsql
stable
as $$
begin
  return query
  select
    dc.chunk_id,
    dc.content,
    dc.metadata,
    1 - (dc.embedding <=> query_embedding) as similarity,
    dc.embedding
  from compass.ollama_document_chunks dc
  where dc.embedding is not null
    and 1 - (dc.embedding <=> query_embedding) > match_threshold
  order by dc.embedding <=> query_embedding
  limit match_count;
end;
$$;
