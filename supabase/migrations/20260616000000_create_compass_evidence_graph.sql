  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

CREATE OR REPLACE FUNCTION compass.promote_learning_feedback_to_resolved_case(
  p_learning_feedback_id bigint,
  p_issue_summary text DEFAULT NULL,
  p_resolution_summary text DEFAULT NULL,
  p_vendor text DEFAULT NULL,
  p_claim_type text DEFAULT 'resolved_issue',
  p_reviewed_by text DEFAULT NULL
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = compass, public
AS $$
DECLARE
  feedback_row compass.learning_feedback%ROWTYPE;
  new_case_id uuid;
  final_issue_summary text;
  final_resolution_summary text;
  final_vendor text;
  final_claim_type text;
BEGIN
  SELECT *
    INTO feedback_row
    FROM compass.learning_feedback
   WHERE id = p_learning_feedback_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'learning_feedback % was not found', p_learning_feedback_id;
  END IF;

  final_issue_summary := NULLIF(trim(coalesce(p_issue_summary, feedback_row.question, '')), '');
  final_resolution_summary := NULLIF(trim(coalesce(p_resolution_summary, feedback_row.answer, '')), '');
  final_vendor := NULLIF(upper(trim(coalesce(p_vendor, feedback_row.learning_payload->>'sourceVendor', ''))), '');
  final_claim_type := coalesce(NULLIF(trim(p_claim_type), ''), 'resolved_issue');

  IF final_issue_summary IS NULL OR final_resolution_summary IS NULL THEN
    RAISE EXCEPTION 'issue_summary and resolution_summary are required';
  END IF;

  IF final_claim_type NOT IN (
    'definition',
    'requirement',
    'prohibition',
    'allowance',
    'limit',
    'procedure',
    'asset_spec',
    'resolved_issue',
    'risk',
    'setup_step'
  ) THEN
    RAISE EXCEPTION 'unsupported claim_type: %', final_claim_type;
  END IF;

  INSERT INTO compass.resolved_cases (
    conversation_id,
    message_id,
    feedback_id,
    question,
    issue_summary,
    resolution_summary,
    resolution_status,
    resolved_by,
    resolved_at,
    approved_for_retrieval,
    sources,
    metadata
  )
  VALUES (
    feedback_row.conversation_id,
    feedback_row.message_id,
    feedback_row.id,
    coalesce(feedback_row.question, final_issue_summary),
    final_issue_summary,
    final_resolution_summary,
    'resolved',
    p_reviewed_by,
    now(),
    true,
    coalesce(feedback_row.sources, '[]'::jsonb),
    jsonb_build_object(
      'learning_feedback_id', feedback_row.id,
      'feedback_key', feedback_row.feedback_key,
      'learning_target', feedback_row.learning_target,
      'promoted_by_function', 'promote_learning_feedback_to_resolved_case'
    )
  )
  RETURNING id INTO new_case_id;

  INSERT INTO compass.evidence_assertions (
    claim_text,
    claim_type,
    source_kind,
    case_id,
    excerpt,
    vendor,
    evidence_decision,
    confidence,
    review_status,
    metadata
  )
  VALUES (
    final_resolution_summary,
    final_claim_type,
    'resolved_case',
    new_case_id,
    final_issue_summary || E'\n\n' || final_resolution_summary,
    final_vendor,
    'verified',
    coalesce(feedback_row.confidence, 0.8),
    'approved',
    jsonb_build_object(
      'learning_feedback_id', feedback_row.id,
      'feedback_key', feedback_row.feedback_key,
      'question', feedback_row.question,
      'source_kind', 'resolved_case'
    )
  );

  UPDATE compass.learning_feedback
     SET learning_status = 'applied',
         reviewed_by = p_reviewed_by,
         reviewed_at = now(),
         applied_at = now(),
         learning_payload = jsonb_set(
           coalesce(learning_payload, '{}'::jsonb),
           '{resolved_case_id}',
           to_jsonb(new_case_id::text),
           true
         ),
         updated_at = now()
   WHERE id = p_learning_feedback_id;

  RETURN new_case_id;
END;
$$;

