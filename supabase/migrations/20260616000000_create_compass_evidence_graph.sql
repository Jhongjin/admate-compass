  ON compass.resolved_cases
  FOR SELECT
  TO authenticated
  USING (approved_for_retrieval = true AND resolution_status = 'resolved');

DROP POLICY IF EXISTS "Service role can manage graph retrieval logs" ON compass.graph_retrieval_logs;
