-- Compass Evidence Graph sidecar
-- Keeps official guide evidence and approved operational issue resolutions in a
-- graph-shaped layer that complements existing vector/keyword RAG retrieval.

CREATE SCHEMA IF NOT EXISTS compass;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS compass.evidence_nodes (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  node_type text NOT NULL CHECK (
    node_type IN (
      'vendor',
      'source_document',
      'evidence_chunk',
      'policy_rule',
      'ad_object',
      'condition',
      'resolved_case',
      'tool',
      'data_requirement'
    )
  ),
  canonical_key text NOT NULL UNIQUE,
  label text NOT NULL,
  vendor text,
  aliases jsonb NOT NULL DEFAULT '[]'::jsonb,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS compass.resolved_cases (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  conversation_id text,
  message_id text,
  feedback_id bigint,
  question text NOT NULL,
  issue_summary text NOT NULL,
  resolution_summary text NOT NULL,
  resolution_status text NOT NULL DEFAULT 'candidate' CHECK (
    resolution_status IN ('candidate', 'resolved', 'rejected', 'needs_official_check')
  ),
  resolved_by text,
  resolved_at timestamptz,
  approved_for_retrieval boolean NOT NULL DEFAULT false,
  sources jsonb NOT NULL DEFAULT '[]'::jsonb,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS compass.evidence_assertions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  claim_text text NOT NULL,
  claim_type text NOT NULL CHECK (
    claim_type IN (
      'definition',
      'requirement',
      'prohibition',
      'allowance',
      'limit',
      'procedure',
      'asset_spec',
      'resolved_issue',
      'risk',
      'setup_step'
    )
  ),
  source_kind text NOT NULL CHECK (source_kind IN ('official_doc', 'resolved_case')),
  source_document_id text,
  source_chunk_id text,
  case_id uuid REFERENCES compass.resolved_cases(id) ON DELETE SET NULL,
  excerpt text,
  source_url text,
  vendor text,
  evidence_decision text NOT NULL DEFAULT 'verified' CHECK (
    evidence_decision IN ('verified', 'weak', 'rejected')
  ),
  confidence numeric NOT NULL DEFAULT 0.8 CHECK (confidence >= 0 AND confidence <= 1),
  review_status text NOT NULL DEFAULT 'approved' CHECK (
    review_status IN ('candidate', 'approved', 'rejected', 'stale')
  ),
  valid_from timestamptz,
  valid_to timestamptz,
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS compass.evidence_edges (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  subject_node_id uuid NOT NULL REFERENCES compass.evidence_nodes(id) ON DELETE CASCADE,
  predicate text NOT NULL CHECK (
    predicate IN (
      'MENTIONS',
      'SUPPORTED_BY',
      'APPLIES_TO',
      'HAS_CONDITION',
      'REQUIRES',
      'PROHIBITS',
      'ALLOWS',
      'LIMITS',
      'RESOLVED_BY',
      'CONFLICTS_WITH',
      'SUPERSEDES'
    )
  ),
  object_node_id uuid NOT NULL REFERENCES compass.evidence_nodes(id) ON DELETE CASCADE,
  evidence_assertion_id uuid REFERENCES compass.evidence_assertions(id) ON DELETE SET NULL,
  polarity text NOT NULL DEFAULT 'affirmative' CHECK (polarity IN ('affirmative', 'negative')),
  confidence numeric NOT NULL DEFAULT 0.8 CHECK (confidence >= 0 AND confidence <= 1),
  review_status text NOT NULL DEFAULT 'approved' CHECK (
    review_status IN ('candidate', 'approved', 'rejected', 'stale')
  ),
  metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS compass.graph_retrieval_logs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  conversation_id text,
  message_id text,
  query text NOT NULL,
  vendor_intent text[] NOT NULL DEFAULT '{}'::text[],
  query_terms text[] NOT NULL DEFAULT '{}'::text[],
  assertion_ids uuid[] NOT NULL DEFAULT '{}'::uuid[],
  retrieval_payload jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS evidence_nodes_node_type_idx ON compass.evidence_nodes(node_type);
CREATE INDEX IF NOT EXISTS evidence_nodes_vendor_idx ON compass.evidence_nodes(vendor);
CREATE INDEX IF NOT EXISTS evidence_nodes_label_fts_idx
  ON compass.evidence_nodes
  USING gin (to_tsvector('simple', coalesce(label, '') || ' ' || coalesce(vendor, '')));

CREATE INDEX IF NOT EXISTS resolved_cases_status_idx ON compass.resolved_cases(resolution_status);
CREATE INDEX IF NOT EXISTS resolved_cases_approved_idx ON compass.resolved_cases(approved_for_retrieval);
CREATE INDEX IF NOT EXISTS resolved_cases_question_fts_idx
  ON compass.resolved_cases
  USING gin (to_tsvector('simple', coalesce(question, '') || ' ' || coalesce(issue_summary, '') || ' ' || coalesce(resolution_summary, '')));

CREATE INDEX IF NOT EXISTS evidence_assertions_vendor_idx ON compass.evidence_assertions(vendor);
CREATE INDEX IF NOT EXISTS evidence_assertions_source_kind_idx ON compass.evidence_assertions(source_kind);
CREATE INDEX IF NOT EXISTS evidence_assertions_review_status_idx ON compass.evidence_assertions(review_status);
CREATE INDEX IF NOT EXISTS evidence_assertions_source_chunk_idx ON compass.evidence_assertions(source_chunk_id);
CREATE INDEX IF NOT EXISTS evidence_assertions_case_idx ON compass.evidence_assertions(case_id);
CREATE INDEX IF NOT EXISTS evidence_assertions_claim_fts_idx
  ON compass.evidence_assertions
  USING gin (to_tsvector('simple', coalesce(claim_text, '') || ' ' || coalesce(excerpt, '') || ' ' || coalesce(vendor, '')));

CREATE INDEX IF NOT EXISTS evidence_edges_subject_idx ON compass.evidence_edges(subject_node_id);
CREATE INDEX IF NOT EXISTS evidence_edges_object_idx ON compass.evidence_edges(object_node_id);
CREATE INDEX IF NOT EXISTS evidence_edges_predicate_idx ON compass.evidence_edges(predicate);
CREATE INDEX IF NOT EXISTS evidence_edges_assertion_idx ON compass.evidence_edges(evidence_assertion_id);

