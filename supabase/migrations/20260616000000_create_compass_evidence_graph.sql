CREATE POLICY "Authenticated users can view approved evidence nodes"
  ON compass.evidence_nodes
  FOR SELECT
  TO authenticated
  USING (true);

DROP POLICY IF EXISTS "Service role can manage evidence assertions" ON compass.evidence_assertions;
CREATE POLICY "Service role can manage evidence assertions"
  ON compass.evidence_assertions
  FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

DROP POLICY IF EXISTS "Authenticated users can view approved evidence assertions" ON compass.evidence_assertions;
CREATE POLICY "Authenticated users can view approved evidence assertions"
  ON compass.evidence_assertions
  FOR SELECT
  TO authenticated
  USING (
    evidence_decision = 'verified'
    AND review_status = 'approved'
    AND (
      source_kind = 'official_doc'
      OR EXISTS (
        SELECT 1
        FROM compass.resolved_cases rc
        WHERE rc.id = evidence_assertions.case_id
          AND rc.approved_for_retrieval = true
          AND rc.resolution_status = 'resolved'
      )
    )
  );

DROP POLICY IF EXISTS "Service role can manage evidence edges" ON compass.evidence_edges;
CREATE POLICY "Service role can manage evidence edges"
  ON compass.evidence_edges
  FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

DROP POLICY IF EXISTS "Authenticated users can view approved evidence edges" ON compass.evidence_edges;
CREATE POLICY "Authenticated users can view approved evidence edges"
  ON compass.evidence_edges
  FOR SELECT
  TO authenticated
  USING (review_status = 'approved');

DROP POLICY IF EXISTS "Service role can manage resolved cases" ON compass.resolved_cases;
CREATE POLICY "Service role can manage resolved cases"
  ON compass.resolved_cases
  FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

DROP POLICY IF EXISTS "Authenticated users can view approved resolved cases" ON compass.resolved_cases;
